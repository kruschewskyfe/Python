# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

#type()
print("Tipo da variavel inteiro = ", type(numero_inteiro))
print("Tipo da variavel real = ", type(numero_real))

# soma +
soma = 1 + 1
print("1 + 1 = ", soma)

# subtração
subtacao = 1 - 1
print("1 - 1 = ", subtacao)

# multiplicação *
multiplicacao = 2 * 2
print("2 x 2 = ", multiplicacao)

# divisão
divisao = 5 /2
print("5 / 2 = ", divisao)
print("Tipo da variável do resultado da divisão = ", type(divisao))
print("Valor em inteiro = ", int(divisao))

# float()
print("Valor em float = ", float(soma))

# modulo %
modulo = 5 % 2
print("Modulo = ", modulo)

# divisão inteiro //
divisao_inteiro = 5 //2
print("5 // 2 = ", divisao_inteiro)
print("Tipo da variável do resultado da divisão = ", type(divisao_inteiro))